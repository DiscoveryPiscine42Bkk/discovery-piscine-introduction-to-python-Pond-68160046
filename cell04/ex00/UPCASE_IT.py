#!/usr/bin/env python3
# The Shebang line makes the script directly executable.

# 1. Prompt the user for input and store it.
# Note: The input function automatically reads the user's text as a string.
user_word = input("Give me a word: ")

# 2. Convert the input string to uppercase using the .upper() string method
# and display the result.
# The .upper() method works on any string variable or literal.
print(user_word.upper())