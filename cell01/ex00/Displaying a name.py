# 1. Define a variable called first_name and initialize it with your first name.
first_name = "Wil"

# 2. Define another variable called last_name and initialize it with your last name.
last_name = "42"

# 3. Display the variables, followed by a new line.
# We can use an f-string to easily combine the two variables with a space in between.
print(f"{first_name} {last_name}")

